<div class="col-right">
	<h4>Pooling is NOT allowed</h4>
	
	Pooling of sales between two or more people within a store is strictly forbidden. Any infringement discovered during the contest or the audit process will result in disqualification.
	
	<h4>Change of Personal Circumstances</h4>
	
	Should any of your personal information change during the contest period, you will have to advise us by calling	09 575 3272.
	
	<h4>Changing employer?</h4>
	
	Should you, during the contest period, change employer, and should your old and new employer both belong to the same retail chain, you will keep your points scored during your previous employment. You will have to advise us of
	the change of employer on 09 575 3272.
	
	<h4>Changing retail chain?</h4>
	
	Should you, during the contest period, change employer and start working for another retail chain, which is also eligible for the Lumix Rewards 2011 contest, you will have to re-register, but you will keep your points scored during your previous employment. You will have to advise us of the change of employer on 09 575 3272.
	
	<h4>No email address?</h4>
	
	Should you not have an email address or have access to email please call us on 09 575 3272.
