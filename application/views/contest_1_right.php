	<h4>Sell Panasonic Lumix products</h4>
	
	<p>After you have registered and received an email confirming your username  and password, as well as a PDF file, outlining all rules details related to the contest, every eligible Lumix product you sell between 1 November ‘11 and 8 January ‘12 will go towards your score. Points are awarded for product sales within the following categories:</p>
	
	<ol>
		<li><strong>Lumix compact cameras</strong></li>
		<li><strong>Lumix G-Series system cameras</strong></li>
		<li><strong>Digital movie cameras</strong></li>
	</ol>
	
	<p>A detailed list of all products you can score points on, and their point values, can be found in the PDF file sent to you upon registering, or on the ‘Record Sales’ page.</p>
	
	<p class="blue">Note: should you have registered after 1 November, the sales you made between 1 November and date of registration will still be eligible for points. Just enter your sales online as soon as possible.</p>
	
	<h4>Product point values and Extra Points</h4>
	
	<p>Each product has been given two point values. You can earn <strong>20% extra points</strong> by entering your sales online within <strong>10 days</strong> of the sale date.</p>
	
	<table>
		<tr>
			<td><strong>Value A:</strong></td>
			<td>applies if you enter your sales online within 10 days of the sales date (i.e sales date is 12 December, docket to be entered online by 22 December at the very latest).</td>
		</tr>
		
		<tr>
			<td><strong>Value B:</strong></td>
			<td>applies if you enter your sales online more than 10 days after the sales date, but by no later than 10 January 2012.</td>
		</tr>
	</table>
	
	<p>As a rule-of-thumb we suggest to enter your sales of any one week online on the Friday or Saturday of that week. This way, all your sales of that week will qualify for the 20% extra points.</p>
